let passed = 0;
let failed = 0;
const rows: Array<{ test: string; expected: string; observed: string; ok: boolean }> = [];

function record(test: string, expected: number, observed: number, tol: number): boolean {
  const diff = Math.abs(expected - observed);
  const ok = diff <= tol;
  rows.push({
    test,
    expected: expected.toFixed(4),
    observed: observed.toFixed(4),
    ok,
  });
  if (ok) passed += 1;
  else failed += 1;
  return ok;
}

function header(s: string): void {
  console.log(`\n=== ${s} ===`);
}

// -----------------------------------------------------------------------------
// 1. Closed-form polynomial-regression spine (§1, §5)
// -----------------------------------------------------------------------------

header('1. Closed-form Gaussian-conjugate evidence (§1)');

for (const ref of NOTEBOOK_TABLE) {
  const X = polynomialDesignMatrix(X_GRID, ref.d);
  const observed = marginalLikelihoodGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  record(`marginalLikelihood d=${ref.d}`, ref.logP, observed, 1e-3);
}

// -----------------------------------------------------------------------------
// 2. Laplace approximation = closed-form for Gaussian conjugate (§3 Theorem 2)
// -----------------------------------------------------------------------------

header('2. Laplace approximation matches closed-form (Gaussian conjugate, §3)');

for (const ref of [NOTEBOOK_TABLE[2], NOTEBOOK_TABLE[3], NOTEBOOK_TABLE[8]]) {
  // d ∈ {3, 4, 9}: Laplace must equal closed-form to numerical precision for
  // Gaussian-conjugate models (notebook §1 asserts allclose at atol=1e-10).
  // We compare against the full-precision closed-form rather than the
  // 4-decimal printed reference.
  const X = polynomialDesignMatrix(X_GRID, ref.d);
  const { mean, precision } = polynomialPosteriorMoments(X, Y_FIXED, SIGMA2, TAU2);
  const closedForm = marginalLikelihoodGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  const jointLogProb = (theta: number[]): number =>
    polynomialJointLogProb(theta, X, Y_FIXED, SIGMA2, TAU2);
  const observed = laplaceApproximation(jointLogProb, mean, precision);
  record(`laplace = closed-form d=${ref.d}`, closedForm, observed, 1e-10);
}

