// -----------------------------------------------------------------------------
// 4. Bits-back identity (Monte Carlo, §10 Theorem 8 verification)
// -----------------------------------------------------------------------------

header('4. Bits-back identity bits-back-net = -ELBO (Monte Carlo, §10)');

{
  const d = 4;
  const X = polynomialDesignMatrix(X_GRID, d);
  const { mean, precision } = polynomialPosteriorMoments(X, Y_FIXED, SIGMA2, TAU2);
  const std = precision.map((_, i) => Math.sqrt(1 / precision[i][i]));
  const elbo = meanFieldELBOGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  const logEvidence = marginalLikelihoodGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  const qSampler = makeMeanFieldGaussianSampler(mean, std, 20260508);
  const logQ = (theta: number[]): number => meanFieldGaussianLogDensity(theta, mean, std);
  const logPrior = (theta: number[]): number => {
    let bb = 0;
    for (let i = 0; i < theta.length; i++) bb += theta[i] * theta[i];
    return -0.5 * theta.length * Math.log(2 * Math.PI * TAU2) - 0.5 * bb / TAU2;
  };
  const logLikelihood = (theta: number[]): number => {
    let sse = 0;
    for (let i = 0; i < N; i++) {
      let pred = 0;
      for (let j = 0; j < theta.length; j++) pred += X[i][j] * theta[j];
      const r = Y_FIXED[i] - pred;
      sse += r * r;
    }
    return -0.5 * N * Math.log(2 * Math.PI * SIGMA2) - 0.5 * sse / SIGMA2;
  };
  const { codeLength } = bitsBackCodingExpectedLength(
    qSampler,
    logPrior,
    logQ,
    logLikelihood,
    logEvidence,
    50_000,
  );
  record('bits-back net = -ELBO at d=4', -elbo, codeLength, 0.05);
}

// -----------------------------------------------------------------------------
// 5. IWELBO at K=1 ≈ ELBO; K large → log p(y) (§8 Theorem 6)
// -----------------------------------------------------------------------------

header('5. IWELBO monotone tightness (Monte Carlo, §8)');

{
  const d = 4;
  const X = polynomialDesignMatrix(X_GRID, d);
  const { mean, precision } = polynomialPosteriorMoments(X, Y_FIXED, SIGMA2, TAU2);
  const std = precision.map((_, i) => Math.sqrt(1 / precision[i][i]));
  const elbo = meanFieldELBOGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  const logEvidence = marginalLikelihoodGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);
  const logJoint = (theta: number[]): number =>
    polynomialJointLogProb(theta, X, Y_FIXED, SIGMA2, TAU2);
  const logQ = (theta: number[]): number => meanFieldGaussianLogDensity(theta, mean, std);

  const qSampler1 = makeMeanFieldGaussianSampler(mean, std, 11111);
  const observed1 = iwelboEstimate(qSampler1, logJoint, logQ, 1, 2000);
  record('IWELBO K=1 ≈ ELBO', elbo, observed1, 0.10);

  const qSampler1000 = makeMeanFieldGaussianSampler(mean, std, 22222);
  const observed1000 = iwelboEstimate(qSampler1000, logJoint, logQ, 1000, 200);
  // IWELBO at K=1000 should be tighter than K=1; checked below the closed-form
  // value with a lenient one-sided tolerance.
  const ok = observed1000 > elbo - 0.10 && observed1000 < logEvidence + 0.10;
  rows.push({
    test: 'IWELBO K=1000 strictly tighter than ELBO',
    expected: `(${elbo.toFixed(2)}, ${logEvidence.toFixed(2)}]`,
    observed: observed1000.toFixed(4),
    ok,
  });
  if (ok) passed += 1;
  else failed += 1;
}

// -----------------------------------------------------------------------------
// 6. AIS at T=100 ≈ closed-form log p(y) (§9 Theorem 7)
// -----------------------------------------------------------------------------

header('6. AIS converges to closed-form (Monte Carlo, §9)');

{
  const d = 4;
  const X = polynomialDesignMatrix(X_GRID, d);
  const logEvidence = marginalLikelihoodGaussianRegression(X, Y_FIXED, SIGMA2, TAU2);

  // Exact-Gaussian sampling at each schedule step β: posterior precision is
  // X^T X / σ² · β + I / τ², with mean β · cov · X^T y / σ².
  const aisRng = mulberry32(33333);
  let buf: number | null = null;
  const draw = (): number => {
    if (buf !== null) {
      const v = buf;
      buf = null;
      return v;
    }
    const [a, b] = gaussianPair(aisRng);
    buf = b;
    return a;
  };
  const dParam = d + 1;

  // Pre-compute X^T X and X^T y for the schedule
  const XtX = new Array<number>(dParam * dParam).fill(0);
  for (let i = 0; i < dParam; i++) {
    for (let j = 0; j < dParam; j++) {
      let acc = 0;
      for (let k = 0; k < N; k++) acc += X[k][i] * X[k][j];
      XtX[i * dParam + j] = acc;
    }
  }
  const Xty = new Array<number>(dParam).fill(0);
  for (let i = 0; i < dParam; i++) {
    let acc = 0;
    for (let k = 0; k < N; k++) acc += X[k][i] * Y_FIXED[k];
    Xty[i] = acc;
  }

  const exactSampleAtBeta = (beta: number): number[] => {
    const prec: number[][] = [];
    for (let i = 0; i < dParam; i++) {
      const row = new Array<number>(dParam).fill(0);
      for (let j = 0; j < dParam; j++) {
        row[j] = (XtX[i * dParam + j] / SIGMA2) * beta;
        if (i === j) row[j] += 1 / TAU2;
      }
      prec.push(row);
    }
    // Solve prec μ = β X^T y / σ² → μ
    const rhs = new Array<number>(dParam).fill(0);
    for (let i = 0; i < dParam; i++) rhs[i] = (Xty[i] / SIGMA2) * beta;
    const Lp: number[][] = [];
    for (let i = 0; i < dParam; i++) {
      const row = new Array<number>(dParam).fill(0);
      let dii = prec[i][i];
      for (let j = 0; j < i; j++) {
        let acc = prec[i][j];
        for (let k = 0; k < j; k++) acc -= row[k] * Lp[j][k];
        row[j] = acc / Lp[j][j];
        dii -= row[j] * row[j];
      }
      row[i] = Math.sqrt(dii);
      Lp.push(row);
    }
    const yTri = new Array<number>(dParam).fill(0);
    for (let i = 0; i < dParam; i++) {
      let acc = rhs[i];
      for (let j = 0; j < i; j++) acc -= Lp[i][j] * yTri[j];
      yTri[i] = acc / Lp[i][i];
    }
    const mu = new Array<number>(dParam).fill(0);
    for (let i = dParam - 1; i >= 0; i--) {
      let acc = yTri[i];
      for (let j = i + 1; j < dParam; j++) acc -= Lp[j][i] * mu[j];
      mu[i] = acc / Lp[i][i];
    }
    // Sample θ = μ + Lp^{-T} z, z ∼ N(0, I)
    const z = new Array<number>(dParam).fill(0);
    for (let i = 0; i < dParam; i++) z[i] = draw();
    const offset = new Array<number>(dParam).fill(0);
    for (let i = dParam - 1; i >= 0; i--) {
      let acc = z[i];
      for (let j = i + 1; j < dParam; j++) acc -= Lp[j][i] * offset[j];
      offset[i] = acc / Lp[i][i];
    }
    const theta = new Array<number>(dParam).fill(0);
    for (let i = 0; i < dParam; i++) theta[i] = mu[i] + offset[i];
    return theta;
  };

  const prior = (): number[] => exactSampleAtBeta(0); // β=0 → just the prior
  const logPrior = (theta: number[]): number => {
    let bb = 0;
    for (let i = 0; i < theta.length; i++) bb += theta[i] * theta[i];
    return -0.5 * dParam * Math.log(2 * Math.PI * TAU2) - 0.5 * bb / TAU2;
  };
  const logLikelihood = (theta: number[]): number => {
    let sse = 0;
    for (let i = 0; i < N; i++) {
      let pred = 0;
      for (let j = 0; j < dParam; j++) pred += X[i][j] * theta[j];
      const r = Y_FIXED[i] - pred;
      sse += r * r;
    }
    return -0.5 * N * Math.log(2 * Math.PI * SIGMA2) - 0.5 * sse / SIGMA2;
  };

  const observed = aisEstimate(
    prior,
    logPrior,
    logLikelihood,
    100,
    (_theta, beta) => exactSampleAtBeta(beta),
    200,
  );
  record('AIS T=100 → closed-form log p(y)', logEvidence, observed, 0.30);
}

// -----------------------------------------------------------------------------
// 7. BIC asymptotic equivalence (sanity check, brief §3 Corollary 1)
// -----------------------------------------------------------------------------

header('7. BIC sanity (§3 Corollary 1)');

{
  // log p(y | θ̂) at d=4 equals the joint log-prob at MAP minus log p(θ̂)
  const d = 4;
  const X = polynomialDesignMatrix(X_GRID, d);
  const { mean } = polynomialPosteriorMoments(X, Y_FIXED, SIGMA2, TAU2);
  // log p(y | θ̂_MLE, M_d) — for a Gaussian-conjugate model with weakly
  // informative prior, the MAP and MLE roughly coincide; we use the MAP here.
  let sse = 0;
  for (let i = 0; i < N; i++) {
    let pred = 0;
    for (let j = 0; j < d + 1; j++) pred += X[i][j] * mean[j];
    const r = Y_FIXED[i] - pred;
    sse += r * r;
  }
  const logLikAtMAP = -0.5 * N * Math.log(2 * Math.PI * SIGMA2) - 0.5 * sse / SIGMA2;
  const negHalfBIC = bicApproximation(logLikAtMAP, d + 1, N);
  // -BIC/2 should be on the same order as log p(y|M_4); the gap is the O(1)
  // Bayesian content (a few nats). Range check: -50 < negHalfBIC < 50.
  const ok = Math.abs(negHalfBIC) < 50;
  rows.push({
    test: '-BIC/2 finite and on order of log p',
    expected: `|·| < 50`,
    observed: negHalfBIC.toFixed(4),
    ok,
  });
  if (ok) passed += 1;
  else failed += 1;
}

